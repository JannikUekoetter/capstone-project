import Head from "next/head";
import styled from "styled-components";
import LostItem from "../components/LostItem";
import FoundItem from "../components/LostItem";
import { lostItems, foundItems } from "../components/Db";
import { useState } from "react";

export default function Home() {
  const [search1, setSearch1] = useState("");
  const [search2, setSearch2] = useState("");

  return (
    <div>
      <Head>
        <title>My App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Main>
        <h1>My App</h1>
        <div>
          <h4>Search1</h4>
          <input
            placeholder="What are you looking for?"
            onChange={(event) => setSearch1(event.target.value)}
          ></input>
        </div>
        <ul>
          {lostItems
            .filter((lostItem) => lostItem.name.toLowerCase().includes(search1))
            .map((lostItem) => (
              <LostItem
                name={lostItem.name}
                description={lostItem.description}
                img={lostItem.img}
                key={lostItem.id}
                id={lostItem.id}
              />
            ))}
        </ul>
        <h2>Second Array of Dummy Data begins here:</h2>
        <div>
          <h4>Search2</h4>
          <input
            placeholder="What are you looking for?"
            onChange={(event) => setSearch2(event.target.value)}
          ></input>
        </div>
        <ul>
          {foundItems
            .filter((foundItem) =>
              foundItem.name.toLowerCase().includes(search2)
            )
            .map((foundItem) => (
              <FoundItem
                name={foundItem.name}
                description={foundItem.description}
                img={foundItem.img}
                key={foundItem.id}
                id={foundItem.id}
              />
            ))}
        </ul>
      </Main>
    </div>
  );
}

const Main = styled.main`
  text-align: center;
`;
